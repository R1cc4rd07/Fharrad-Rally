				<p style="font-size: 0.6rem;">&#169; Ipf- und Jagst-Zeitung</p>
				<p>Bei der Ellwanger Fahrrad-Rallye war das Motto dieses Mal:  Groß-Britannien. Weshalb die Queen höchst persönlich zur Siegerehrung in die Scheune nach Schönau gekommen ist. <br>
				<br>
				<span class="underscore"><b>Ellwangen:</b></span> Es ist schon ein bewegender Moment gewesen, als die Queen in der  Kutsche vorfuhr, um in majestätischer Würde die Sieger der Ellwanger  Fahrrad-Rallye zu ehren. Seit über 30 Jahren organisieren Robert Ziegler und Robert Lay diesen ganz besonderen Ausflug. Er steht immer unter  einem Motto. Dieses Mal war es Groß-Britannien.<br>
				<br>
				Einmal im Jahr treibt sich ein bunt kostümiertes Völkchen der Stadt herum. Mal waren es Afrikaner, mal Bayern, mal war das Motto Mittelalter,  Hitparade, Tausendundeine Nacht, Hollywood oder Ellwangen. Als was man  sich da verkleidet? Als Oberbürgermeister natürlich, sagt Dieter Groß, Ex-Präsident des FCV, der jetzt auch in der Organisation der  Fahrrad-Rallye mitmischt. Lange haben das die Robert Zeigler und Robert Lay alleine gemacht, das erste Mal vor über 30 Jahren.<br>
				<br>
				Erfunden wurde die Fahrradrallye in Bierlaune im <b>Frosch</b>, der längst das  Zeitliche gesegnet hat. Im Gegensatz zur Fahrrad-Rallye, die allerdings  einmal zehn Jahre lang pausiert hat, als beide Organisatoren in München gearbeitet haben.<br>
				<br>
				Seit 2003 treffen sich die Teams aber wieder jedes  Jahr an einem Wochenende im Mai im Bistro, um von dort zu starten. Ziel  ist immer die Scheune von Brigitte Taschek in Schönau, wo nicht nur die  Sieger geehrt, sondern auch die Abschlussparty gefeiert wird. Mit  riesigem Lagerfeuer.<br>
				<br>
				Nur einmal, 2005, ist die  Rallye ausgefallen. Das Wetter war zu schlecht. Und dieses Jahr wurde  sie auf September verschoben. Es gab zu viele Terminüberschneidungen.<br>
				<br>
				Im Schnitt sind 20, 21 Teams am Start, die sich und ihr Fahrrad passend  zum Motto kostümieren und an den Stationen auch kleine Vorführungen  machen. Die Gruppe Wimbledon um Bistro-Wirt Joti Agathangelidis hatte  einen mobilen Tennisplatz dabei. Die Theatermenschen kamen als “€žDinner for one” und haben bei jeder Wertungsprüfung den Sketch aufgeführt.  Jockeys sind angetreten und Dieter Groß als Golfer in Knickerbockern.<br>
				</p>
				<h2>Pferderennen mit Steckenpferden</h2>
				<p>Bei seiner Wertungsprüfung auf dem Wagnershof konnten die Teilnehmer mit  perfektem Abschlag und beim Putten Punkte holen. Anderswo mussten sie  einen Fuchs zusammenpuzzlen, aus sechs Teilen, die Aufgabe war also  machbar. Im Schloss war - Ascot lässt grüßen - Pferderrennen mit  Steckenpferden und einem vollen Glas Sekt angesagt, auf dem Sportplatz  Rindelbach musste angelehnt an die Highland Games ein kleines Fass  gerollt werden und zum Abschluss wurde Darts mit Kinderpfeilen gespielt.<br>
				<br>
				Denn die Fahrradrallye ist nicht nur für Erwachsene, sondern auch für Kinder. Deshalb sind die Strecken eher kurz. Dieses Mal waren es 18  Kilometer. Zeitlimit gibt es keins, nur die Siegerehrung um 18 Uhr in  Schönau. Mitradeln kann jeder. Vielleicht werden es im nächsten Mai ja  mal 25 Teams, wünschen sich Ziegler und Groß. Mit dabei ist dann auf  jeden Fall wieder ein Ehepaar aus Düsseldorf. Das hatte beim Start im  Bistro so viel Spaß, dass es im nächsten Mai wiederkommen will.<br>
				<br>
				Mehr Informationen und viele Fotos gibt es auf der Homepage unter www.fahrrad-rally-ellwangen.de</p>
